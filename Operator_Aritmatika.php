<?php

// Operator Aritmatika
$a = 10;
$b = 10;

//Penambahan
$result = $a + $b;

echo "Operator Penambahan \n";
echo "10 + 10 = ";
var_dump ($result);

// Pengurangan
$result = $a - $b;

echo "Operator Pengurangan \n";
echo "10 - 10 = ";
var_dump ($result);

// Perkalian
$result = $a * $b;

echo "Operator Perkalian \n";
echo "10 * 10 = ";
var_dump ($result);

// Pembagian
$result = $a / $b;

echo "Operator Pembagian \n";
echo "10 / 10 = ";
var_dump ($result);

// Modulo(Sisa Bagi)
$result = $a % $b;

echo "Operator Modulo(Sisa Bagi) \n";
echo "10 % 10 = ";
var_dump ($result);

// Pangkat
$result = 2 ** 2;

echo "Operator Pangkat \n";
echo "1 ** 2 = ";
var_dump ($result);

/*
Operator Aritmatika
- Di gunakan untuk melakukan operasi matematika
- Terdapat beberapa operasi aritmatika 
    1. + (Tambah)
    2. - (Kurang)
    3. / (Bagi)
    4. % (Modulo/Sisa bagi)
    5. ** (Pangkat)
*/